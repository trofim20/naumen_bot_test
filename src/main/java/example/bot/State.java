package example.bot;

/**
 * Состояние
 */
public enum State {
    /**
     * Начальное состояние
     */
    INIT,
    /**
     * Режим прохождения теста
     */
    TEST,
    /**
     * Повтор вопросов, на которые был дан неправильный ответ
     */
    REPEAT,
    /**
     * Настройка напоминания: текст напоминания
     */
    SET_NOTIFY_TEXT,
    /**
     * Настройка напоминания: через сколько секунд отправить напоминание
     */
    SET_NOTIFY_DELAY
}
